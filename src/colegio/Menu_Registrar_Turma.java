    private void botao_eliminar_cursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_eliminar_cursoActionPerformed

        int index = Tabela.getSelectedRow();
        Tabela.getSelectedRow();
        Turma turm = new Turma();
        if (Tabela.getSelectedRow() != -1) {
            DefaultTableModel conteudos = (DefaultTableModel) Tabela.getModel();
            String nome_turma = conteudos.getValueAt(index, 0).toString();
            for (Turma selecionado : turmas) {
                if (selecionado.getDescricao().equals(nome_turma)) {
                    turmas.remove(selecionado);
                     JOptionPane.showMessageDialog(null, turm.salvar(turmas));
                    break;
                }
            }
            conteudos.removeRow(Tabela.getSelectedRow());

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para eliminar algum estudante");
        }
    }//GEN-LAST:event_botao_eliminar_cursoActionPerformed

    private void botao_consultar_cursosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_consultar_cursosActionPerformed
        if(txt_pesquisar_turma.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o nome para ser pesquisado");
        }
        txt_pesquisar_turma.requestFocus();
        DefaultTableModel t = (DefaultTableModel) Tabela.getModel();
        int linhas_tabela = t.getRowCount();

        for (int x = linhas_tabela - 1; x >= 0; x--) {
            t.removeRow(x);
        }

        for (Turma consulta : turmas) {
            if (consulta.getDescricao().contains(txt_pesquisar_turma.getText())) {
                Object[] dados_turma
                = {
                    consulta.getDescricao(), consulta.getNumero(),
                    consulta.getTurno(),
                    consulta.getDirector_turma()
                };
                t.addRow(dados_turma);
                txt_pesquisar_turma.setText(null);
            }
        }
    }//GEN-LAST:event_botao_consultar_cursosActionPerformed

