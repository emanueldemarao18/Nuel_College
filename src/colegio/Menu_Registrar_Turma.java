    private void Registrar_TurmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Registrar_TurmaActionPerformed
       
      
         
        if(Txt_descricao_turma.getText().equals("")
            || Txt_descricao_turma.getText().equals("")
            || Txtturno.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Campo em falta ");
        } else {
            x++;
            Turma t = new Turma();
            t.setDescricao(Txt_descricao_turma.getText());
            t.setDirector_turma(director.getSelectedItem().toString());
            t.setTurno(Txtturno.getText());
            t.setNumero(x);

            numm.setValue(x);
          

            turmas.add(t);
            JOptionPane.showMessageDialog(null,t.salvar(turmas));

            JOptionPane.showMessageDialog(null, "Turma cadastrada com Sucesso");
            Txt_descricao_turma.setText(null);
            numm.setValue(0);
            Txt_descricao_turma.setText(null);
            Txtturno.setText(null);

        }
    }//GEN-LAST:event_Registrar_TurmaActionPerformed

