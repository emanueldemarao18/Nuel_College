    private void Listar_TurmasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Listar_TurmasActionPerformed
        DefaultTableModel t = (DefaultTableModel) Tabela.getModel();
        int linhas = t.getRowCount();

        for (int i = linhas-1; i >=0; i--) {
            t.removeRow(i);
        }

        for (Turma dados : turmas) {

            Object[] lista_turmas
            = {
                dados.getDescricao(), dados.getNumero(), dados.getTurno(),
                dados.getDirector_turma()
              
                
            };
            t.addRow(lista_turmas);
        }
    }//GEN-LAST:event_Listar_TurmasActionPerformed

