    private void Registrar_TurmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Registrar_TurmaActionPerformed

        if (Txt_descricao_disciplina.getText().equals("") || TXTcursos.getSelectedItem()==null || prof.getSelectedItem()==null ) {
            System.out.print("campo faltando cara");
            JOptionPane.showMessageDialog(null, "Campo em falta ");
        } else {

            Disciplina d = new Disciplina();
            d.setDescricao(Txt_descricao_disciplina.getText());
            d.setCargaHoraria((Integer) Txt_carga_horaria.getValue());
            d.setCurso(TXTcursos.getSelectedItem().toString());
            d.setProfessor(prof.getSelectedItem().toString());

            disciplinas.add(d);
            JOptionPane.showMessageDialog(null, d.salvar(disciplinas));

            JOptionPane.showMessageDialog(null, "Turma cadastrada com Sucesso");
            Txt_descricao_disciplina.setText(null);

        }
    }//GEN-LAST:event_Registrar_TurmaActionPerformed

