    private void botao_actualizar_disciplinaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_actualizar_disciplinaActionPerformed
        if (txt_pesquisar_disciplina.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Digite o nome para ser pesquisado");
        }
        txt_pesquisar_disciplina.requestFocus();
        DefaultTableModel t = (DefaultTableModel) Tabela.getModel();
        int linhas_tabela = t.getRowCount();

        for (int x = linhas_tabela - 1; x >= 0; x--) {
            t.removeRow(x);
        }

        for (Disciplina consulta : disciplinas) {
            if (consulta.getDescricao().contains(txt_pesquisar_disciplina.getText())) {
                Object[] dados_turma
                        = {
                            consulta.getDescricao(),consulta.getCargaHoraria(),
                            consulta.getCurso(),
                            consulta.getProfessor()
                        };
                t.addRow(dados_turma);
                txt_pesquisar_disciplina.setText(null);
            }
        }
    }//GEN-LAST:event_botao_actualizar_disciplinaActionPerformed

    private void botao_eliminar_cursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_eliminar_cursoActionPerformed

        int index = Tabela.getSelectedRow();
        Tabela.getSelectedRow();
        if (Tabela.getSelectedRow() != -1) {
            DefaultTableModel conteudos = (DefaultTableModel) Tabela.getModel();
            String nome_disciplina = conteudos.getValueAt(index, 0).toString();
            for (Disciplina selecionado : disciplinas) {
                if (selecionado.getDescricao().equals(nome_disciplina)) {
                    disciplinas.remove(selecionado);
                    break;
                }
            }
            conteudos.removeRow(Tabela.getSelectedRow());

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para eliminar algum estudante");
        }
    }//GEN-LAST:event_botao_eliminar_cursoActionPerformed

    private void Listar_disciplinaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Listar_disciplinaActionPerformed
        DefaultTableModel t = (DefaultTableModel) Tabela.getModel();
        int linhas = t.getRowCount();

        for (int i = linhas - 1; i >= 0; i--) {
            t.removeRow(i);
        }

        for (Disciplina dados : disciplinas) {

            Object[] lista_turmas
                    = {
                        dados.getDescricao(),
                        dados.getCargaHoraria(),
                        dados.getCurso(),
                        dados.getProfessor()
                        

                    };
            t.addRow(lista_turmas);
        }
    }//GEN-LAST:event_Listar_disciplinaActionPerformed

