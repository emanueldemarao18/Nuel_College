    private void Botao_registrar_docenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Botao_registrar_docenteActionPerformed
        if(Txtnome_docente.getText().equals("")
            || altura.getText().equals("")
            || peso.getText().equals("")
            || Txt_disciplina.getSelectedItem()==null){
            JOptionPane.showMessageDialog(null, "Campo em falta ");
        } else {
            x++;
            Docente doce = new Docente();
            doce.setNome(Txtnome_docente.getText());
            doce.setIdade((Integer) Idade.getValue());
            doce.setGenero(Sexo.getSelectedItem().toString());
            doce.setAltura(altura.getText());
            doce.setPeso(peso.getText());
            doce.setEstado_civil(Estado.getSelectedItem().toString());
            doce.setFuncao_exercida(Txt_funcao.getSelectedItem().toString());
            doce.setDisciplina(Txt_disciplina.getSelectedItem().toString());
            doce.setTurma(turma.getSelectedItem().toString());
            doce.setSalario(salario.getText());
            doce.setNumero_docente(x);
           
            num.setValue(x);
            docentes.add(doce);
            JOptionPane.showMessageDialog(null, doce.salvar(docentes));
            

            JOptionPane.showMessageDialog(null, "Docente Cadastrado com Sucesso");
            Txtnome_docente.setText(null);
            Idade.setValue(6);
            altura.getText();

        }
    }//GEN-LAST:event_Botao_registrar_docenteActionPerformed

