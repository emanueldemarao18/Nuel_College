    private void botao_eliminar_docenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_eliminar_docenteActionPerformed

        int index = Tabela.getSelectedRow();
        Tabela.getSelectedRow();
        if (Tabela.getSelectedRow() != -1) {
            DefaultTableModel conteudos = (DefaultTableModel) Tabela.getModel();
            String nome_docente = conteudos.getValueAt(index, 0).toString();
            for (Docente selecionado : docentes) {
                if (selecionado.getNome().equals(nome_docente)) {
                    docentes.remove(selecionado);
                    break;
                }
            }
            conteudos.removeRow(Tabela.getSelectedRow());

        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma linha para eliminar algum estudante");
        }
    }//GEN-LAST:event_botao_eliminar_docenteActionPerformed

    private void botao_consultar_docenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botao_consultar_docenteActionPerformed
        if(txt_nome_consultar_docente.getText().equals(null)) {
            JOptionPane.showMessageDialog(null, "Digite o nome para ser pesquisado");
        }
        txt_nome_consultar_docente.requestFocus();
        DefaultTableModel t = (DefaultTableModel) Tabela.getModel();
        int linhas_tabela = t.getRowCount();

        for (int x = linhas_tabela - 1; x >= 0; x--) {
            t.removeRow(x);
        }

        for (Docente consulta : docentes) {
            if (consulta.getNome().contains(txt_nome_consultar_docente.getText())) {
                Object[] dados_estudante
                = {
                    consulta.getNome(), consulta.getIdade(), consulta.getGenero(),
                    consulta.getAltura(), consulta.getPeso(), consulta.getEstado_civil(),
                    consulta.getNumero_docente(), consulta.getDisciplina(), consulta.getTurma(),
                    consulta.getFuncao_exercida(),consulta.getSalario()
                };
                t.addRow(dados_estudante);
            }
        }
    }//GEN-LAST:event_botao_consultar_docenteActionPerformed

